# Numbers
a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)

# Strings
text = "Hello"
name = "Taha"

greeting = text + " " + name
print(greeting)

print("Uppercase:", greeting.upper())
print("Lowercase:", greeting.lower())
print("Is alphabetic?", greeting.replace(" ", "").isalpha())
